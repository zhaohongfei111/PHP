	public $socket;
	
	public function __construct($host, $port) {
 		$this->socket = socket_create ( AF_INET, SOCK_STREAM, SOL_TCP );
		socket_connect ( $this->socket, $host, $port ) or die('connect error!');
		socket_set_option ( $this->socket, SOL_TCP, 1, 0 );
	}
	
	
	/**
	 * @see BigEndianBuffer::writeBytes()
	 *
	 * @param bytes $bytes
	 */
	public function writeBytes($bytes) {
		socket_write ( $this->socket, $bytes );
	}
	
	public function writeChars($chars) {
		$args = array (str_repeat ( 'C', count($chars) ));
		$args = array_merge($args,$chars);
		$binarys = call_user_func_array ( 'pack', $args );
		$this->writeBytes ( $binarys );
	}
	
	
	
